// True negative — Math.random() used for non-crypto concerns (shuffle, UI).
// The enclosing function body contains no crypto-context tokens.

export function shuffleItems<T>(items: T[]): T[] {
  const arr = [...items];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    const tmp = arr[i];
    arr[i] = arr[j];
    arr[j] = tmp;
  }
  return arr;
}
